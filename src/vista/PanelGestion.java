    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int sel = this.jComboBox1.getSelectedIndex();
        if (sel == 0) {
            this.pcliente.generarTabla(this.cliente.bid(Integer.valueOf(this.jTextField1.getText())));
            if(this.jComboBox2.getSelectedIndex()==0)
            this.preservacion.generarTabla(this.reservacion.getReservacion(Integer.valueOf(this.jTextField1.getText())));
            if(this.jComboBox2.getSelectedIndex()==1)
            this.preservacion.generarTabla(this.reservacion.getPCobrar(Integer.valueOf(this.jTextField1.getText())));
            if(this.jComboBox2.getSelectedIndex()==2)
            this.preservacion.generarTabla(this.reservacion.getFinalizado(Integer.valueOf(this.jTextField1.getText())));
            if(this.jComboBox2.getSelectedIndex()==3){                
            this.preservacion.generarTabla(this.reservacion.getAnulado(Integer.valueOf(this.jTextField1.getText())));
            }
            
        }
        if (sel == 1) {
            this.pcliente.generarTabla(this.cliente.bdpi(Integer.valueOf(this.jTextField1.getText())));
            this.preservacion.generarTabla(this.cliente.bid(Integer.valueOf(this.jTextField1.getText())));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

