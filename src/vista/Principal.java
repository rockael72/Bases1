    private String girarFecha(String fecha) {
        String[] fecha2 = {"", "", ""};
        int cont = 0;
        for (int i = 0; i < fecha.length(); i++) {

            if (fecha.charAt(i) != '/') {
                fecha2[cont] += fecha.charAt(i);
            } else {
                cont++;
            }
        }
        fecha = fecha2[2] + "/" + fecha2[1] + "/" + fecha2[0];
        return fecha;
    }

